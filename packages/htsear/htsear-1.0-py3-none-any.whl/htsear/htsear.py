def sear(text):
    text = print(text)
    return text

def sear_upr(text):
    text = print(text.upper())
    return text

def sear_lwr(text):
    text = print(text.lower())
    
def sear_cap(text):
    text = print(text.capitalize())
    
def sear_ln(text):
    text = print(len(text))
    return text