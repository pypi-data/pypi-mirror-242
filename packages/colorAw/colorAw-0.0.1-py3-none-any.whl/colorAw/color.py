class Color:
    black = "\033[0;30m"
    red = "\033[0;31m"
    green = "\033[0;32m"
    yellow = "\033[;33m"
    blue = "\033[0;34m"
    purple = "\033[0;35m"
    cyan = "\033[0;36m"
    white = "\033[0;37m"

    class bold:
        bold_black = "\033[1;30m"
        bold_red = "\033[1;31m"
        bold_green = "\033[1;32m"
        bold_yellow = "\033[1;33m"
        bold_blue = "\033[0;34m"
        bold_purple = "\033[1;35m"
        bold_cyan = "\033[1;36m"
        bold_white = "\033[1;37m"

    class background():
        black = "\033[40m"
        red = "\033[41m"
        green = "\033[42m"
        yellow = "\033[43m"
        blue = "\033[44m"
        purple = "\033[45m"
        cyan = "\033[46m"
        white = "\033[47m"
